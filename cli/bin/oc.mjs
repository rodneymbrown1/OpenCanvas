#!/usr/bin/env node

/**
 * Open Canvas CLI
 *
 * Commands:
 *   oc init [dir]     Initialize a new workspace
 *   oc start          Launch Open Canvas (browser + PTY server)
 *   oc format [dir]   Convert documents to markdown
 */

import { spawn } from "node:child_process";
import { existsSync, mkdirSync, writeFileSync, copyFileSync } from "node:fs";
import { join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const ROOT = resolve(__dirname, "../..");

const args = process.argv.slice(2);
const command = args[0];

const HELP = `
  Open Canvas CLI

  Usage:
    oc init [dir]     Initialize a new workspace
    oc start          Launch browser UI + PTY server
    oc format [dir]   Convert docs to markdown (coming soon)
    oc help           Show this help

  Examples:
    oc init my-project
    oc start
`;

const DEFAULT_YAML = `# Open Canvas Workspace
# Generated by: oc init

workspace:
  root: "."

agent:
  active: "claude"
  claude:
    mode: "cli"
    permissions: { read: true, write: true, execute: true, web: true }
  codex:
    mode: "cli"
    permissions: { read: true, write: true, execute: true, web: true }
  gemini:
    mode: "cli"
    permissions: { read: true, write: true, execute: true, web: true }

api_keys: {}
mcp_servers: []

server:
  port: 3000
  pty_port: 3001
  auth_token: ""

preferences:
  persist_jobs: true
  default_stack: "nextjs-tailwind"
  theme: "dark"
`;

switch (command) {
  case "init": {
    const dir = resolve(args[1] || ".");
    console.log(`\n  Initializing Open Canvas workspace in: ${dir}\n`);

    if (!existsSync(dir)) {
      mkdirSync(dir, { recursive: true });
    }

    // Create workspace structure
    const dirs = ["data", "apps"];
    for (const d of dirs) {
      const p = join(dir, d);
      if (!existsSync(p)) {
        mkdirSync(p, { recursive: true });
        console.log(`  Created ${d}/`);
      }
    }

    // Write config
    const configPath = join(dir, "open-canvas.yaml");
    if (!existsSync(configPath)) {
      writeFileSync(configPath, DEFAULT_YAML);
      console.log("  Created open-canvas.yaml");
    } else {
      console.log("  open-canvas.yaml already exists, skipping");
    }

    console.log("\n  Done! Run `oc start` to launch Open Canvas.\n");
    break;
  }

  case "start": {
    console.log("\n  Starting Open Canvas...\n");

    // Start PTY server
    const pty = spawn("node", [join(ROOT, "server/pty-server.mjs")], {
      stdio: "inherit",
      cwd: ROOT,
    });

    // Start Next.js dev server
    const next = spawn("npx", ["next", "dev", "--port", "3000"], {
      stdio: "inherit",
      cwd: ROOT,
    });

    // Open browser after a short delay
    setTimeout(() => {
      const openCmd =
        process.platform === "darwin"
          ? "open"
          : process.platform === "win32"
          ? "start"
          : "xdg-open";
      spawn(openCmd, ["http://localhost:3000"], { stdio: "ignore" });
    }, 3000);

    // Handle shutdown
    const cleanup = () => {
      pty.kill("SIGTERM");
      next.kill("SIGTERM");
      process.exit(0);
    };
    process.on("SIGINT", cleanup);
    process.on("SIGTERM", cleanup);
    break;
  }

  case "format": {
    console.log("\n  Format command coming soon.");
    console.log("  For now, use your coding agent to format documents.\n");
    break;
  }

  case "help":
  case undefined:
  case "--help":
  case "-h":
    console.log(HELP);
    break;

  default:
    console.log(`\n  Unknown command: ${command}`);
    console.log(HELP);
    process.exit(1);
}
